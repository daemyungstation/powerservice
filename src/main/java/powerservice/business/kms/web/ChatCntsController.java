    /**
     * 문자메세지 유형정보를 등록/수정한다.
     *
     * @param pmParam Map<String, Object>
     * @return ModelAndView
     * @throws Exception
     */
    @RequestMapping(value = "/save")
    @ResponseBody
    public ModelAndView insertChatCnts(XPlatformMapDTO xpDto, Model model) throws Exception {
        ModelAndView modelAndView = new ModelAndView("xplatformMapView");

        // 에러코드및 메시지
        String szErrorCode = "0";
        String szErrorMsg  = "OK";

        try{
            Map <String, Object> mapInVar     = xpDto.getInVariableMap();
            Map <String, DataSetMap> mapInDs  = xpDto.getInDataSetMap();
            Map <String, Object> mapOutVar    = xpDto.getOutVariableMap();
            Map <String, DataSetMap> mapOutDs = xpDto.getOutDataSetMap();

            DataSetMap listInDs = (DataSetMap)mapInDs.get("ds_input");
            if (listInDs.size() > 0) {
                Map hmParam = listInDs.get(0);

                String sChatCntsId = StringUtils.defaultString((String) hmParam.get("chat_cnts_id"));

                ParamUtils.addSaveParam(hmParam);
                ParamUtils.addCenterParam(hmParam);

                @SuppressWarnings("unchecked")
                List<String> fileids = (List<String>) hmParam.get("fileIds");

                if (fileids != null && fileids.size() > 0) {
                    String sAddr = basVlService.getBasVlAsString("file_download_url", (String) hmParam.get("cntr_cd"));
                    String sImgAddr = sAddr+fileids.get(0);
                    hmParam.put("img_url_addr", sImgAddr);
                }

                if ("".equals(sChatCntsId)) {
                    sChatCntsId = chatCntsService.insertChatCnts(hmParam);
                } else {
                    chatCntsService.updateChatCnts(hmParam);
                }
                Map mParam  = new HashMap<String, Object>();
                ParamUtils.addCenterParam(mParam);
                mParam.put("chat_cnts_id", sChatCntsId);

                Map<String, Object> mData = chatCntsService.getChatCnts(mParam);
                // FAQ 상세정보 조회
                DataSetMap dsList = new DataSetMap();

                dsList.setRowMaps(mData);
                mapOutDs.put("ds_output", dsList);

            }
            modelAndView.addObject(XPlatformConstant.OUT_VARIABLES_ATT_NAME, xpDto.getOutVariableMap());
            modelAndView.addObject(XPlatformConstant.OUT_DATASET_ATT_NAME,   xpDto.getOutDataSetMap());
        }catch(Exception e){
            e.printStackTrace();
            szErrorCode ="-1";
            szErrorMsg = e.getMessage();
        }

        modelAndView.addObject(XPlatformConstant.ERROR_CODE, szErrorCode);
        modelAndView.addObject(XPlatformConstant.ERROR_MSG,  szErrorMsg);

        return modelAndView;
    }

    /**
     * 문자메세지 유형정보를 삭제한다.
     *
     * @param pmParam Map<String, Object>
     * @return ModelAndView
     * @throws Exception
     */
    @RequestMapping(value = "/delete")
    @ResponseBody
    public ModelAndView deleteChatCnts(XPlatformMapDTO xpDto, Model model) throws Exception {
        ModelAndView modelAndView = new ModelAndView("xplatformMapView");
        Map<String, Object> hmParam = new HashMap<String, Object>();

        // 에러코드및 메시지
        String szErrorCode = "0";
        String szErrorMsg  = "OK";


        try {
            Map <String, DataSetMap> mapInDs  = xpDto.getInDataSetMap();

            DataSetMap listInDs = (DataSetMap)mapInDs.get("ds_input");
            hmParam = listInDs.get(0);
            chatCntsService.deleteChatCnts(hmParam);
        }catch (Exception e) {
            e.printStackTrace();
            szErrorCode = "-1";
            szErrorMsg  = e.getMessage();
        }

        modelAndView.addObject(XPlatformConstant.ERROR_CODE, szErrorCode);
        modelAndView.addObject(XPlatformConstant.ERROR_MSG,  szErrorMsg);
        return modelAndView;
    }

